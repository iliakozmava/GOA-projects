# 5) მაგალითები მათემატიკურ ოპერაციებზე

# დამატება (+)
print("დამატების მაგალითები:")
print(1 + 2)
print(4 + 5)
print(10 + 3)
print(7 + 8)
print(6 + 6)

# გამოკლება (-)
print("გამოკლების მაგალითები:")
print(5 - 2)
print(9 - 3)
print(10 - 7)
print(8 - 1)
print(15 - 5)

# გამრავლება (*)
print("გამრავლების მაგალითები:")
print(2 * 3)
print(4 * 5)
print(6 * 2)
print(3 * 3)
print(5 * 1)

# გაყოფა (/)
print("გაყოფის მაგალითები:")
print(10 / 2)
print(9 / 3)
print(8 / 2)
print(6 / 3)
print(12 / 4)

# მთელზე გაყოფა (//)
print("მთელზე გაყოფის მაგალითები:")
print(10 // 3)
print(9 // 4)
print(8 // 5)
print(7 // 2)
print(6 // 4)

# ნაშთი (%)
print("ნაშთის მაგალითები:")
print(10 % 3)
print(9 % 4)
print(8 % 5)
print(7 % 2)
print(6 % 4)

# ხარისხი (**)
print("ხარისხის მაგალითები:")
print(2 ** 2)
print(3 ** 2)
print(2 ** 3)
print(4 ** 2)
print(5 ** 2)
